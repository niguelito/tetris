const shapes = [];
const colors = [];
const metadata = [];

function createShape(color, shape, name, credit) {
    // 8th color needs to be red
    if (colors.length == 7) colors.push("#FF0000")

    const id = colors.length + 1;

    metadata.push({name: name, credit: credit, shape: shape, color: color, id: id});

    // replace all 1s in shape with the id
    for (let i = 0; i < shape.length; i++) {
        for (let j = 0; j < shape[i].length; j++) {
            if (shape[i][j] === 1) {
                shape[i][j] = id;
            }
        }
    }

    shapes.push(shape);
    colors.push(color);
}

function getShapes() {
    return shapes;
}

function getColors() {
    return colors;
}

/**
 * 
 * @returns {{name: string, credit: string, shape: number[][], color: string, id: number}[]} metadata
 */
function getMetadata() {
    return metadata;
}

